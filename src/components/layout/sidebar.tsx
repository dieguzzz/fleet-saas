export function Sidebar() {
  const pathname = usePathname();
  const org = useCurrentOrg();
  const role = useCurrentRole();

  if (!org) return null;

  const navItems = getNavItems(org.slug);

  return (
    <aside className="w-64 bg-slate-900 text-white min-h-screen flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-slate-800">
        <Link href={`/${org.slug}`} className="flex items-center gap-3">
          <div className="w-10 h-10 bg-gradient-to-br from-blue-500 to-purple-600 rounded-lg flex items-center justify-center font-bold text-lg">
            {org.name.charAt(0).toUpperCase()}
          </div>
          <div>
            <h2 className="font-semibold truncate max-w-[140px]">{org.name}</h2>
            <span className="text-xs text-slate-400 capitalize">{role}</span>
          </div>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4">
        <ul className="space-y-1">
          {navItems.map((item) => {
            // Check permission if required
            if (item.permission && !hasPermission(role, item.permission)) {
              return null;
            }

            const isActive = pathname === item.href;

            return (
              <li key={item.href}>
                <Link
                  href={item.href}
                  className={cn(
                    'flex items-center gap-3 px-3 py-2.5 rounded-lg transition-colors',
                    isActive
                      ? 'bg-blue-600 text-white'
                      : 'text-slate-300 hover:bg-slate-800 hover:text-white'
                  )}
                >
                  {item.icon}
                  <span className="font-medium">{item.label}</span>
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-slate-800">
        <p className="text-xs text-slate-500 text-center">
          Fleet SaaS © 2024
        </p>
      </div>
    </aside>
  );
}
